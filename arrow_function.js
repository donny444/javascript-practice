//Normal Function
hello1 = function() {
    return "Hello World!";
}

//Arrow Function
hello2 = () => {
    return "Hello World!";
}

//Shorter Arrow Function (only one statement)
hello3 = () => "Hello World!";

//Arrow Function With Parameters
hello4 = (val) => "Hello " + val;

